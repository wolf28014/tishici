async function seed() {
  console.log('开始种子数据初始化...')

  // 1. 创建分类
  console.log('创建分类...')
  for (const cat of CATEGORIES) {
    await db.category.upsert({
      where: { name: cat.name },
      update: { description: cat.description, icon: cat.icon, color: cat.color, sortOrder: cat.sortOrder },
      create: cat,
    })
  }
  const categories = await db.category.findMany()
  console.log(`已创建 ${categories.length} 个分类`)

  // 2. 清空旧 prompts（如重跑种子）
  await db.prompt.deleteMany({})

  // 3. 创建 prompts
  console.log('创建提示词...')
  for (const p of PROMPTS) {
    const cat = categories.find(c => c.name === p.categoryName)
    if (!cat) {
      console.warn(`未找到分类: ${p.categoryName}`)
      continue
    }
    await db.prompt.create({
      data: {
        title: p.title,
        content: p.content,
        description: p.description,
        categoryId: cat.id,
        tags: JSON.stringify(p.tags),
        isPinned: p.isPinned ?? false,
        isFavorite: p.isFavorite ?? false,
        author: p.author ?? 'PromptHub',
        source: p.source ?? 'seed',
      },
    })
  }
  const promptCount = await db.prompt.count()
  console.log(`已创建 ${promptCount} 条提示词`)

  console.log('种子数据初始化完成！')
}

seed()
  .catch(e => {
    console.error('种子数据初始化失败：', e)
    process.exit(1)
  })
  .finally(async () => {
    await db.$disconnect()
  })
